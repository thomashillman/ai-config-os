#!/usr/bin/env node
/**
 * generate-commands.mjs — Generate .claude/commands/<skill>.md from compiled skills.
 *
 * For each skill that is available or degraded (per capability probe), writes a
 * markdown command file to .claude/commands/. Removes stale generated commands
 * for skills that are no longer applicable. User-authored commands are preserved
 * (tracked via a generated-skills registry file).
 *
 * Variant selection (in priority order):
 *   1. CLAUDE_MODEL env var  → extract model family (opus / sonnet / haiku)
 *   2. surface_hint from probe:
 *        mobile / ios / web-mobile → prefer haiku
 *        desktop-cli / claude-code → prefer sonnet
 *   3. fallback_chain first entry
 *   4. 'sonnet' hard default
 *
 * Usage:
 *   node adapters/claude/generate-commands.mjs [--project-dir <dir>] [--dry-run]
 */

import { readFileSync, writeFileSync, mkdirSync, readdirSync, existsSync, rmSync } from 'node:fs';
import { join, dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { classifyAll, loadProbeResults } from './filter-skills.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));

// ─── Default paths ─────────────────────────────────────────────────────────

const HOME           = process.env.HOME || process.env.USERPROFILE || '/tmp';
const DEFAULT_PROBE  = join(HOME, '.ai-config-os', 'probe-report.json');
const DEFAULT_CACHE  = join(HOME, '.ai-config-os', 'cache', 'claude-code');

/** Registry file that tracks which command files were generated (vs user-authored). */
const REGISTRY_FILE  = '.generated-skills.json';

/** Marker embedded in every generated command file. */
const GENERATED_MARKER = 'generated by ai-config-os';

// ─── Surface → variant preference map ─────────────────────────────────────

const SURFACE_VARIANT_PREFERENCE = {
  'mobile':              'haiku',
  'ios':                 'haiku',
  'web-mobile':          'haiku',
  'claude-ios':          'haiku',
  'desktop-cli':         'sonnet',
  'claude-code-remote':  'sonnet',
  'claude-code':         'sonnet',
  'web':                 'sonnet',
  'desktop':             'sonnet',
};

// ─── Model hint extraction ─────────────────────────────────────────────────

/**
 * Extract model family (opus | sonnet | haiku) from a model ID string.
 * Returns '' if unrecognised.
 */
export function extractModelFamily(modelId) {
  if (!modelId) return '';
  const lower = modelId.toLowerCase();
  if (lower.includes('haiku'))  return 'haiku';
  if (lower.includes('sonnet')) return 'sonnet';
  if (lower.includes('opus'))   return 'opus';
  return '';
}

// ─── Variant selection ─────────────────────────────────────────────────────

/**
 * Select the best variant key for a skill given the current model and surface.
 *
 * @param {object} skill        — skill entry from manifest (must have .variants)
 * @param {string} modelHint    — model family: 'opus' | 'sonnet' | 'haiku' | ''
 * @param {string} surfaceHint  — surface_hint from probe report
 * @returns {string}            — variant key (e.g. 'sonnet')
 */
export function selectVariant(skill, modelHint, surfaceHint) {
  const variants = skill.variants || {};
  const variantKeys = Object.keys(variants).filter(k => k !== 'fallback_chain');
  const fallbackChain = Array.isArray(variants.fallback_chain)
    ? variants.fallback_chain.filter(k => variantKeys.includes(k))
    : [];

  // Helper: return key if it exists in this skill's variants, else ''
  const pick = (key) => (key && variantKeys.includes(key) ? key : '');

  // 1. Explicit model hint takes priority
  if (modelHint) {
    const exact = pick(modelHint);
    if (exact) return exact;
    // Model not available → walk fallback_chain
    if (fallbackChain.length) return fallbackChain[0];
  }

  // 2. Surface-based preference (no model hint, or model not matched)
  const surfacePreference = SURFACE_VARIANT_PREFERENCE[surfaceHint] || '';
  if (surfacePreference) {
    const exact = pick(surfacePreference);
    if (exact) return exact;
  }

  // 3. fallback_chain first entry
  if (fallbackChain.length) return fallbackChain[0];

  // 4. Hard default: sonnet, then any available variant
  return pick('sonnet') || variantKeys[0] || 'sonnet';
}

// ─── Content builder ───────────────────────────────────────────────────────

/**
 * Build the markdown content for a .claude/commands/<skill>.md file.
 *
 * @param {string} skillId    — skill identifier
 * @param {string} description — one-line description from frontmatter
 * @param {string} body       — markdown body from SKILL.md (after frontmatter)
 * @param {string} variantKey — selected variant key
 * @returns {string}
 */
export function buildCommandContent(skillId, description, body, variantKey) {
  const lines = [
    `<!-- ${GENERATED_MARKER} | skill: ${skillId} | variant: ${variantKey} -->`,
    '',
    `# ${skillId}`,
    '',
    description.trim(),
    '',
    `> variant: ${variantKey}`,
    '',
    body.trim(),
    '',
  ];
  return lines.join('\n');
}

// ─── File I/O helpers ──────────────────────────────────────────────────────

function loadJSON(filePath) {
  try {
    return JSON.parse(readFileSync(filePath, 'utf8'));
  } catch {
    return null;
  }
}

function loadSkillMd(skillsDir, skillId) {
  const filePath = join(skillsDir, skillId, 'SKILL.md');
  if (!existsSync(filePath)) return null;
  const raw = readFileSync(filePath, 'utf8');
  const match = raw.match(/^---\r?\n[\s\S]*?\r?\n---\r?\n?([\s\S]*)$/);
  return match ? match[1].trim() : raw.trim();
}

// ─── Main generator ────────────────────────────────────────────────────────

/**
 * Generate .claude/commands/*.md for all available/degraded skills.
 *
 * @param {object} options
 * @param {string}  options.projectDir   — root of the project (default: cwd)
 * @param {string}  [options.cacheDir]   — override cache directory path
 * @param {string}  [options.probeFile]  — override probe report path
 * @param {string}  [options.commandsDir]— override commands output path
 * @param {string}  [options.modelHint]  — override model family (opus/sonnet/haiku)
 * @param {boolean} [options.dryRun]     — if true, log but don't write files
 * @returns {Promise<{ written: string[], removed: string[], skipped: string[] }>}
 */
export async function generateCommands({
  projectDir  = process.cwd(),
  cacheDir    = DEFAULT_CACHE,
  probeFile   = DEFAULT_PROBE,
  commandsDir,
  modelHint,
  dryRun      = false,
} = {}) {
  const resolvedCommandsDir = commandsDir || join(projectDir, '.claude', 'commands');

  // ── Load probe report ────────────────────────────────────────────────────
  const probe = loadProbeResults(probeFile);
  const surfaceHint = probe.surface_hint || 'desktop-cli';

  // ── Resolve model hint ───────────────────────────────────────────────────
  const resolvedModelHint = modelHint
    ?? extractModelFamily(process.env.CLAUDE_MODEL || '');

  // ── Load manifest ────────────────────────────────────────────────────────
  const manifest = loadJSON(join(cacheDir, 'latest.json'));
  if (!manifest || !Array.isArray(manifest.skills)) {
    // Nothing to do; don't crash
    return { written: [], removed: [], skipped: [] };
  }

  const skillsDir = join(cacheDir, 'skills');

  // ── Classify skills against probe ─────────────────────────────────────────
  const bucketById = new Map(
    classifyAll(manifest.skills, probe.supported).map(c => [c.id, c.bucket])
  );

  // ── Ensure commands dir exists ───────────────────────────────────────────
  if (!dryRun) {
    mkdirSync(resolvedCommandsDir, { recursive: true });
  }

  // ── Load previously generated registry ───────────────────────────────────
  const registryPath = join(resolvedCommandsDir, REGISTRY_FILE);
  const prevGenerated = new Set((loadJSON(registryPath) || []).filter(Boolean));

  // ── Classify and generate ─────────────────────────────────────────────────
  const written  = [];
  const skipped  = [];
  const nowGenerated = new Set();

  for (const skill of manifest.skills) {
    const bucket = bucketById.get(skill.id) ?? 'available';

    if (bucket === 'excluded' || bucket === 'unavailable') {
      skipped.push(skill.id);
      continue;
    }

    // Load SKILL.md body
    const body = loadSkillMd(skillsDir, skill.id);
    if (!body) {
      skipped.push(skill.id);
      continue;
    }

    const variantKey = selectVariant(skill, resolvedModelHint, surfaceHint);
    const description = (skill.description || '').replace(/\\n/g, ' ').trim();
    const content = buildCommandContent(skill.id, description, body, variantKey);

    const outPath = join(resolvedCommandsDir, `${skill.id}.md`);
    nowGenerated.add(skill.id);

    if (!dryRun) {
      writeFileSync(outPath, content, 'utf8');
    }
    written.push(skill.id);
  }

  // ── Remove stale generated commands ──────────────────────────────────────
  const removed = [];
  for (const staleId of prevGenerated) {
    if (!nowGenerated.has(staleId)) {
      const stalePath = join(resolvedCommandsDir, `${staleId}.md`);
      if (existsSync(stalePath)) {
        if (!dryRun) rmSync(stalePath);
        removed.push(staleId);
      }
    }
  }

  // ── Persist registry ──────────────────────────────────────────────────────
  if (!dryRun) {
    writeFileSync(registryPath, JSON.stringify([...nowGenerated].sort(), null, 2), 'utf8');
  }

  return { written, removed, skipped };
}

// ─── CLI entry point ───────────────────────────────────────────────────────

if (process.argv[1] && resolve(process.argv[1]) === resolve(fileURLToPath(import.meta.url))) {
  const args     = process.argv.slice(2);
  const dryRun   = args.includes('--dry-run');
  const verbose  = args.includes('--verbose');
  const pdIdx    = args.indexOf('--project-dir');
  const projectDir = pdIdx !== -1 ? args[pdIdx + 1] : process.cwd();

  generateCommands({ projectDir, dryRun })
    .then(({ written, removed, skipped }) => {
      if (written.length)  console.log(`[generate-commands] Written: ${written.join(', ')}`);
      if (removed.length)  console.log(`[generate-commands] Removed stale: ${removed.join(', ')}`);
      if (verbose && skipped.length) console.log(`[generate-commands] Skipped: ${skipped.join(', ')}`);
      if (!written.length && !removed.length) {
        console.log('[generate-commands] No changes.');
      }
    })
    .catch(err => {
      console.error(`[generate-commands] Error: ${err.message}`);
      process.exit(1);
    });
}
